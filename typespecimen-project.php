        <h2>About the Project</h2>
        <p>The purpose of this assignment was to experiment with the different aspects of typographic hierarchy, which include weight, linespacing, position, size and color, in order to effectively communicate the unique characteristics of a specific typeface. In order to successfully portray the typeface, we were required to do a significant amount of research about its history and its uses. Our goal was to clearly present our information and discover how typographical signals serve as visual cues for hierarchy and association.
        </p>
        <br>

        <h2>Project Process</h2>
        <p> In beginning to create my poster, I wanted to base my project off of the fact that Avenir is the French word for “Future”. My original idea was to focus more on a theme that included the French flag and the poster really focused on highlighting some of the unique physical characteristics of the Avenir typeface. I took inspiration from this <a href='http://www.behance.net/gallery/Avenir-Type-Specimen/2035743'>sample Avenir type specimen</a>. I liked the layering of the graphics between the letters and the simplicity of the overall design. After completion of my first iteration, I decided to change the color scheme of the poster and make the right side less busy. The “French” design was too literal and the character set was hard to read. I took inspiration from this <a href='http://www.behance.net/gallery/Avenir-Type-Specimen/3272223'>sample Avenir type specimen</a> for my next iteration. In my final iteration, I used the solid blocks of colors and lines with softer corners in attempt to maintain a futuristic feel. I took the layered styling from the first iteration to help accentuate the name of the typeface. My goal was for people to read the quote first, then move towards the name of the typeface.
        </p>
